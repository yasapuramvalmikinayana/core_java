class IncrementOperatorDemo{

public static void main(String[] args){

      int x = 10;
      int y = x+1;
    
     System.out.println(++y) ;
     System.out.println(++x) ;
     System.out.println(x) ;
     System.out.println(y) ;

     int m = 500;
     int n = 600;
  System.out.println(n++) ;
  System.out.println(m++) ;
  System.out.println(m++) ;
  System.out.println(++n) ;
  System.out.println(m) ;
  System.out.println(n) ;
}
}
